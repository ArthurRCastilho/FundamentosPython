"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
String são textos dentro de aspas
"""

#Aspas Simples
print('Luis "Otávio"')

#Aspas Duplas
print("Luis Otávio") #Linguagem fica confusa, por tanto não é possivel colocar Otávio dentro de Aspas como na print anterios


#Escape
print("Luis \"Otávio\"") #a "\"(Barra invertidaa) é utilizada como escape ela pula as aspas pra ignorar aquela. Sendo possivel colocar outra aspas duplas, dentro de uma string com aspas duplas.

#r
print(r"Luis \"Otávio\"")

print('Explícito', 'é', 'melhor " do que implícito')



